// Get business type into right section
const getRightBusinessType = (str) => {
    let food = [];
    let coffee = [];
    let clothing = [];
    let homegoods = [];
    let others = [];
    let arr = str.split(' ');
    let type = [];
    if(arr.includes('Food') || arr.includes('food')){
        type.push('food')
    }
    else if(arr.includes('Coffee') || arr.includes('coffee')){
        type.push('coffee')
    }
    else if(arr.includes('Clothing') || arr.includes('clothing')){
        type.push('clothing')
    }
    else if(arr.includes('Home') || arr.includes('home')){
        type.push('homegoods')
    }
    else {
        type.push('others')
    }
    return type[0]

}



// Add array into object
export const tranformIntoObj = (arr) => {
    arr.forEach(business => {
        business.businessType.name = getRightBusinessType(business.businessType.name)
        if(!obj.all.all.all.includes(business.id)){
            obj.all.all.all.push(business.id);
            obj[business.businessLocation.county].all.all.push(business.id);
            obj[business.businessLocation.county][business.businessType.name].all.push(business.id);
            obj.all[business.businessType.name].all.push(business.id);
            for(const owner of business.ownerTypes){
                obj[business.businessLocation.county].all[owner.name].push(business.id);
                obj.all.all[owner.name].push(business.id);
                obj.all[business.businessType.name][owner.name].push(business.id);
                obj[business.businessLocation.county][business.businessType.name][owner.name].push(business.id);
            }   
        }       

    })
    console.log(obj);
    return obj;
}







